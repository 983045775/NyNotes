public class PointInfo {
    private float mPointX;
    private float mPointY;

    public void setPointX(float pointX) {
        this.mPointX = pointX;
    }

    public void setPointY(float pointY) {
        this.mPointY = pointY;
    }

    public float getPointX() {
        return mPointX;
    }

    public float getPointY() {
        return mPointY;
    }
}